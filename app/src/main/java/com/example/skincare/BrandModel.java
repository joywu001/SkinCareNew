package com.example.skincare;

public class BrandModel {
    String brand;
    int image;

    public BrandModel(String brand, int image) {
        this.brand = brand;
        this.image = image;
    }
    public void setImage(String brand) {
        this.brand = brand;
    }
    public void setImage(int image) {
        this.image = image;
    }

    public String getBrand() {
        return brand;
    }
    public int getImage() {
        return image;
    }

}
